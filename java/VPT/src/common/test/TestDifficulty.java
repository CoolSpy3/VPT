package common.test;

/**
 * Represents the difficulty of a test
 */
public enum TestDifficulty {
    
    /**
     * Represents a test which is intended to be easy and unspecific
     */
    GENERAL,
    /**
     * Represents a test which is intended to be for a beginner in a programming language
     */
    BEGINNER,
    /**
     * Represents a test which is intended to challenge intermediate topics in a programming language
     */
    INTERMEDIATE,
    /**
     * Represents a test which is intended to focus on topics in a programming language
     */
    ADVANCED;
    
}