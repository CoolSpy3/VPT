package common.test;

/**
 * Specifies the programming language for which tests are written
 */
public enum Language {
    
    /**
     * Specifies tests which were not written for a specific programming language
     */
    GENERAL,
    /**
     * Specifies tests which were written for c++
     */
    CPP,
    /**
     * Specifies tests which were written for java
     */
    JAVA;
    
}